         WHEN NOT cm.legacy_exists THEN already_removed
         WHEN NOT cm.snake_exists THEN skip_snake_missing
         WHEN COALESCE(d.dep_count,0) > 0 THEN skip_has_dependencies
         ELSE drop_candidate
       END AS action
FROM col_meta cm
LEFT JOIN deps d ON d.schema_name=cm.schema_name AND d.table_name=cm.table_name AND d.legacy_col=cm.legacy_col
ORDER BY cm.table_name, cm.legacy_col;
