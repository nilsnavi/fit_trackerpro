), deps AS (
  SELECT cm.schema_name, cm.table_name, cm.legacy_col, count(*) AS dep_count
  FROM col_meta cm
  LEFT JOIN pg_depend d ON d.refobjid = cm.relid AND d.refobjsubid = cm.legacy_attnum AND d.deptype IN (n,a,i,e,x)
  GROUP BY cm.schema_name, cm.table_name, cm.legacy_col
)
SELECT cm.table_name, cm.legacy_col, cm.snake_col, cm.legacy_exists, cm.snake_exists, COALESCE(d.dep_count,0) AS dep_count,
