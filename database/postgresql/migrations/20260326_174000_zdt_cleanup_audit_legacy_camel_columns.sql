), col_meta AS (
  SELECT m.*,
         to_regclass(format(%I.%I, m.schema_name, m.table_name)) AS relid,
         (SELECT attnum FROM pg_attribute WHERE attrelid = to_regclass(format(%I.%I, m.schema_name, m.table_name)) AND attname = m.legacy_col AND attnum > 0 AND NOT attisdropped) AS legacy_attnum,
         EXISTS (SELECT 1 FROM information_schema.columns c WHERE c.table_schema=m.schema_name AND c.table_name=m.table_name AND c.column_name=m.legacy_col) AS legacy_exists,
